
public class Organizar_Vetores {
    public static void main(String[] args){
        int vetor[] = new int [5];
        for (int i = 0; i < vetor.length; i++) {
            vetor[i] = i + 1;
        }
        for (int i = 0; i < vetor.length; i++) {
            System.out.print(vetor[i] + " ");
        }
    }
 
}  

